
#=====================================
# 리스트
# list(숫자, 문자, 함수)
#
# 이름으로 원소선택
# a[["name"]]
# a$name
#
# 리스트에서 원소 제거
# a[["name"]] <- NULL
#
# NULL 원소를 리스트에서 제거
# a[sapply(a, is.null)] <- NULL
# a[a==o] <- NULL
# a[is.na(a)] <- NULL
#=====================================

a <- list(v1,v2,v3)
# [[1]]
# [1] "a" "b"
# [[2]]
# [1] "apple"  "banana"
# [[3]]
# [1] "carrot"

a[[1]]   # "a" "b"

a[c(1,3)]
# [[1]]
# [1] "a" "b"
# [[2]]
# [1] "carrot"

